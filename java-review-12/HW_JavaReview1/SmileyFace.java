public class SmileyFace {

	public static void main(String[] args) {
		System.out.println("");
		System.out.print("\t \t \t \t \t /\\__/\\ \n");
		System.out.print("\t \t \t \t \t (=^.^=) \n");
		System.out.print("\t \t \t \t \t (\") (\") \n");
		System.out.println("");
		System.out.println("\t \t \t \t       Little cat \n");
		
		
		System.out.print("\t \t ___\\|/___ \n");
		System.out.println("\t        /  -   -  \\");
		System.out.println("\t       /   o   o   \\");
		System.out.println("\t       |           |");
		System.out.println("\t       \\   \\___/   /");
		System.out.println("\t        \\_________/\n");
		
		System.out.println("\t       Smiley face");
	}

}
